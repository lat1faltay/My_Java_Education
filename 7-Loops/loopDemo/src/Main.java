public class Main {
    public static void main(String[] args) {

        // FOR LOOP
        for(int i=1; i<10;i++){
            System.out.println(i);
        }
        System.out.println("For Loop bitti");


        // While LOOP
        int i = 1;
        while(i<10){
            System.out.println(i);
            i+=1;
        }
        System.out.println("While Loop bitti");


        // Do While Loop
        int j=1;
        do{
            System.out.println(j);
            j+=2;
        }while(j<10);
        System.out.println("Do-While Loop bitti");

       


    }
}